let nomes = [];
//push - insere um elemento fim do array
nomes.push('Matheus');
nomes.push('Bruna');
//unshift - insere um elemento no inicio do array
nomes.unshift('Roni');
//sort - ordena o array
nomes.sort();
console.log(nomes);
//reverse - inverte o array
nomes.reverse();
console.log(nomes);
//pop - remove o ultimo elemento do vetor
//shift - remove o primeiro elemento do vetor
//splice - apaga na posição i e j elementos (i,j)
nomes.splice(0,1);
console.log(nomes);